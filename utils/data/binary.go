package data

const (
	// Ki is a IEC binary prefix
	Ki = 1024
	// Mi is a IEC binary prefix
	Mi = Ki * Ki
	// Gi is a IEC binary prefix
	Gi = Ki * Mi
	// Ti is a IEC binary prefix
	Ti = Ki * Gi
	// Pi is a IEC binary prefix
	Pi = Ki * Ti
	// Ei is a IEC binary prefix
	Ei = Ki * Pi
	// Zi is a IEC binary prefix
	Zi = Ki * Ei
	// Yi is a IEC binary prefix
	Yi = Ki * Zi
)

const (
	// K is a SI decimal prefix
	K = 1000
	// M is a SI decimal prefix
	M = K * K
	// G is a SI decimal prefix
	G = K * M
	// T is a SI decimal prefix
	T = K * G
	// P is a SI decimal prefix
	P = K * T
	// E is a SI decimal prefix
	E = K * P
	// Z is a SI decimal prefix
	Z = K * E
	// Y is a SI decimal prefix
	Y = K * Z
)
